package Sorteo;

public class Participante {
    private int numEntradas;

    public Participante(int numEntradas) {
        this.numEntradas = numEntradas;
    }

    public int getNumEntradas() {
        return numEntradas;
    }

    public void setNumEntradas(int numEntradas) {
        this.numEntradas = numEntradas;
    }

}
